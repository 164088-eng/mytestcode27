import math # ดึงเครื่องมือคณิตศาสตร์มาใช้คำนวณรากที่สอง (Square Root)

กว้าง = float(input("ใส่ความกว้าง: "))
ยาว = float(input("ใส่ความยาว: "))

พื้นที่ = กว้าง * ยาว
เส้นรอบรูป = 2 * (กว้าง + ยาว)
เส้นทแยงมุม = math.sqrt((กว้าง**2) + (ยาว**2)) # สูตร Pythagorean

print(f"1. พื้นที่: {พื้นที่}")
print(f"2. เส้นรอบขอบรูป: {เส้นรอบรูป}")
print(f"3. เส้นทแยงมุม: {เส้นทแยงมุม:.2f}") # แสดงทศนิยม 2 ตำแหน่ง